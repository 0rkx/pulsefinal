# Feature computation — Git (NEW)
# ---------------------------------------------------------------------------
def compute_git_features(git_rows):
    """
    Per (employee, date):
      git_commits_total       — number of commits
      git_lines_added         — total lines added
      git_lines_deleted       — total lines deleted
      git_lines_changed       — added + deleted (proxy for code churn)
      git_distinct_repos      — distinct repos touched
      git_commits_after_hours — commits outside 7am–9pm
      git_commits_weekend     — commits on Sat/Sun
    """
    acc = defaultdict(lambda: {
        "total": 0, "added": 0, "deleted": 0, "repos": set(),
        "after_hours": 0, "weekend": 0,
    })
    for r in git_rows:
        key = (r["employee_id"], r["timestamp"].date())
        a   = acc[key]
        a["total"]   += 1
        a["added"]   += r["lines_added"]
        a["deleted"] += r["lines_deleted"]
        a["repos"].add(r["repo"])
        if _is_after_hours(r["timestamp"]):
            a["after_hours"] += 1
        if _is_weekend(r["timestamp"]):
            a["weekend"] += 1

    return {
        key: {
            "git_commits_total":       a["total"],
            "git_lines_added":         a["added"],
            "git_lines_deleted":       a["deleted"],
            "git_lines_changed":       a["added"] + a["deleted"],
            "git_distinct_repos":      len(a["repos"]),
            "git_commits_after_hours": a["after_hours"],
            "git_commits_weekend":     a["weekend"],
        }
        for key, a in acc.items()
    }

# ---------------------------------------------------------------------------
# Feature computation — Zoom (NEW)
# ---------------------------------------------------------------------------
def compute_zoom_features(zoom_rows):
    """
    Per (employee, date):
      zoom_meetings_count     — number of Zoom sessions
      zoom_total_minutes      — sum of durations
      zoom_speaking_seconds   — total self-speaking time
      zoom_speaking_ratio     — speaking_seconds / (total_meeting_seconds)
                                capped at 1.0; proxy for participation/withdrawal
      zoom_avg_participants   — mean participant count (proxy for meeting size)
    """
    acc = defaultdict(lambda: {
        "count": 0, "minutes": 0, "speaking": 0,
        "participants": [], "total_seconds": 0,
    })
    for r in zoom_rows:
        key = (r["employee_id"], r["date"])
        a   = acc[key]
        a["count"]        += 1
        a["minutes"]      += r["duration_minutes"]
        a["speaking"]     += r["speaking_seconds"]
        a["total_seconds"]+= r["duration_minutes"] * 60
        a["participants"].append(r["participant_count"])

    out = {}
    for key, a in acc.items():
        ratio = (
            round(a["speaking"] / a["total_seconds"], 3)
            if a["total_seconds"] > 0 else 0.0
        )
        avg_p = (
            round(sum(a["participants"]) / len(a["participants"]), 1)
            if a["participants"] else 0.0
        )
        out[key] = {
            "zoom_meetings_count":   a["count"],
            "zoom_total_minutes":    a["minutes"],
            "zoom_speaking_seconds": a["speaking"],
            "zoom_speaking_ratio":   min(1.0, ratio),
            "zoom_avg_participants": avg_p,
        }
    return out

# ---------------------------------------------------------------------------
# Main aggregation
# ---------------------------------------------------------------------------
def main():
    _load_nlp()

    print("Loading raw CSVs...")
    employees = load_employees()
    slack     = load_slack()
    jira      = load_jira()
    calendar  = load_calendar()
    git       = load_git()
    zoom      = load_zoom()
    print(f"  {len(employees)} employees | {len(slack):,} slack | "
          f"{len(jira):,} jira | {len(calendar):,} cal | "
          f"{len(git):,} git | {len(zoom):,} zoom")

    print("Computing per-day features...")
    slack_f = compute_slack_features(slack)
    jira_f  = compute_jira_features(jira)
    cal_f   = compute_calendar_features(calendar)
    git_f   = compute_git_features(git)
    zoom_f  = compute_zoom_features(zoom)

    all_dates  = sorted({r["timestamp"].date() for r in slack})
    start_date = all_dates[0]
    end_date   = all_dates[-1]
    num_days   = (end_date - start_date).days + 1
    print(f"  Date range: {start_date} → {end_date} ({num_days} days)")
    print(f"  NLP mode: {'RoBERTa' if _nlp_available else 'fallback (numeric)'}")

    # Column order — existing columns first (backward compat), then new ones
    feature_columns = [
        # identifiers
        "employee_id", "name", "team", "persona",
        "date", "day_index", "weekday", "is_weekend",
        # slack (original)
        "slack_msgs_total", "slack_msgs_after_hours", "slack_msgs_weekend",
        "slack_msgs_work_channels", "slack_msgs_social_channels",
        "slack_dms_sent", "slack_distinct_channels", "slack_distinct_dm_partners",
        "slack_avg_sentiment",
        # slack (new NLP features)
        "slack_sentiment_volatility",
        "slack_avg_stress_score",
        # jira (original)
        "jira_events", "jira_distinct_tickets", "jira_tickets_closed",
        "jira_story_points_closed", "jira_comments",
        # calendar (original)
        "meetings_count", "meetings_total_minutes", "meetings_attendees_total",
        "meetings_back_to_back", "largest_focus_gap_minutes",
        # derived ratios (original)
        "after_hours_ratio", "social_msg_ratio",
        # git (new)
        "git_commits_total", "git_lines_added", "git_lines_deleted",
        "git_lines_changed", "git_distinct_repos",
        "git_commits_after_hours", "git_commits_weekend",
        # zoom (new)
        "zoom_meetings_count", "zoom_total_minutes",
        "zoom_speaking_seconds", "zoom_speaking_ratio", "zoom_avg_participants",
    ]

    rows_written = 0
    with open(OUTPUT_FILE, "w", newline="") as f:
        w = csv.writer(f)
        w.writerow(feature_columns)

        for eid, emp in employees.items():
            for i in range(num_days):
                d   = start_date + timedelta(days=i)
                key = (eid, d)

                sf = slack_f.get(key, {})
                jf = jira_f.get(key, {})
                cf = cal_f.get(key, {})
                gf = git_f.get(key, {})
                zf = zoom_f.get(key, {})

                msgs_total  = sf.get("slack_msgs_total", 0)
                msgs_after  = sf.get("slack_msgs_after_hours", 0)
                msgs_social = sf.get("slack_msgs_social_channels", 0)

                after_hours_ratio = round(msgs_after  / msgs_total, 3) if msgs_total else 0.0
                social_msg_ratio  = round(msgs_social / msgs_total, 3) if msgs_total else 0.0

                largest_gap = cf.get(
                    "largest_focus_gap_minutes",
                    9 * 60 if not _is_weekend(d) else 0
                )

                w.writerow([
                    # identifiers
                    eid, emp["name"], emp["team"], emp["persona"],
                    d.isoformat(), i, d.weekday(), 1 if _is_weekend(d) else 0,
                    # slack original
                    msgs_total, msgs_after, sf.get("slack_msgs_weekend", 0),
                    sf.get("slack_msgs_work_channels", 0), msgs_social,
                    sf.get("slack_dms_sent", 0),
                    sf.get("slack_distinct_channels", 0),
                    sf.get("slack_distinct_dm_partners", 0),
                    sf.get("slack_avg_sentiment", 0.0),
                    # slack NLP
                    sf.get("slack_sentiment_volatility", 0.0),
                    sf.get("slack_avg_stress_score", 0.0),
                    # jira
                    jf.get("jira_events", 0),
                    jf.get("jira_distinct_tickets", 0),
                    jf.get("jira_tickets_closed", 0),
                    jf.get("jira_story_points_closed", 0),
                    jf.get("jira_comments", 0),
                    # calendar
                    cf.get("meetings_count", 0),
                    cf.get("meetings_total_minutes", 0),
                    cf.get("meetings_attendees_total", 0),
                    cf.get("meetings_back_to_back", 0),
                    largest_gap,
                    # derived
                    after_hours_ratio, social_msg_ratio,
                    # git
                    gf.get("git_commits_total", 0),
                    gf.get("git_lines_added", 0),
                    gf.get("git_lines_deleted", 0),
                    gf.get("git_lines_changed", 0),
                    gf.get("git_distinct_repos", 0),
                    gf.get("git_commits_after_hours", 0),
                    gf.get("git_commits_weekend", 0),
                    # zoom
                    zf.get("zoom_meetings_count", 0),
                    zf.get("zoom_total_minutes", 0),
                    zf.get("zoom_speaking_seconds", 0),
                    zf.get("zoom_speaking_ratio", 0.0),
                    zf.get("zoom_avg_participants", 0.0),
                ])
                rows_written += 1

    print(f"\nWrote {rows_written:,} rows → {OUTPUT_FILE}")
    print(f"  Total feature columns: {len(feature_columns)}")

if __name__ == "__main__":
    main()