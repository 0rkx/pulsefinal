"""
PulseIQ — Step 1: Daily Feature Aggregation (V4.0: ML Upgrade)
===============================================================
Reads raw CSVs and produces daily_features.csv with one row per
(employee, day).

Changes from V3.2:
  - RoBERTa sentiment: replaces the synthetic sentiment_score float
    with real inference from cardiffnlp/twitter-roberta-base-sentiment.
    Graceful fallback to the existing numeric column if transformers/torch
    not installed (so the pipeline never breaks).
  - New NLP features per (employee, day):
      slack_sentiment_volatility  — std of per-message sentiments (mood instability)
      slack_avg_stress_score      — keyword-based burnout language frequency
  - Git aggregation: reads git_commits.csv (generated by generate_data.py)
      git_commits_total, git_lines_added, git_lines_deleted, git_lines_changed,
      git_distinct_repos, git_commits_after_hours, git_commits_weekend
  - Zoom aggregation: reads zoom_meetings.csv
      zoom_meetings_count, zoom_total_minutes, zoom_speaking_seconds,
      zoom_speaking_ratio (speaking / (duration * 60)), zoom_avg_participants

Output format is a strict superset of V3.2 — all existing columns kept,
new columns appended. compute_baselines.py and compute_burnout.py consume
these automatically.

Run AFTER generate_data.py.
"""

import csv
import statistics as _stats
from collections import defaultdict
from datetime import datetime, timedelta
from pathlib import Path

DATA_DIR   = Path("pulseiq_data")
OUTPUT_FILE = DATA_DIR / "daily_features.csv"

AFTER_HOURS_START = 21   # 9 pm
AFTER_HOURS_END   = 7    # 7 am
SOCIAL_CHANNELS   = {"#random", "#general"}

# ---------------------------------------------------------------------------
# RoBERTa sentiment — lazy-loaded; pipeline never breaks without it
# ---------------------------------------------------------------------------
_nlp_pipeline  = None
_nlp_available = False

def _load_nlp():
    global _nlp_pipeline, _nlp_available
    try:
        from transformers import pipeline as hf_pipeline
        print("  Loading RoBERTa sentiment model "
              "(cardiffnlp/twitter-roberta-base-sentiment)...")
        _nlp_pipeline = hf_pipeline(
            "sentiment-analysis",
            model="cardiffnlp/twitter-roberta-base-sentiment",
            tokenizer="cardiffnlp/twitter-roberta-base-sentiment",
            truncation=True,
            max_length=512,
        )
        _nlp_available = True
        print("  RoBERTa loaded ✓")
    except ImportError:
        print("  [WARN] transformers/torch not installed — "
              "using fallback numeric sentiment from CSV.")
        _nlp_available = False

# cardiffnlp label map  LABEL_0=negative  LABEL_1=neutral  LABEL_2=positive
_LABEL_TO_SCORE = {"LABEL_0": 0.1, "LABEL_1": 0.5, "LABEL_2": 0.9}

def _score_text(text: str, fallback: float) -> float:
    """Return 0–1 sentiment. Uses RoBERTa when available, else fallback."""
    if not _nlp_available or not text.strip():
        return fallback
    result = _nlp_pipeline(text[:512])[0]
    base   = _LABEL_TO_SCORE.get(result["label"], 0.5)
    # Weight by model confidence so borderline predictions stay near 0.5
    return round(0.5 + (base - 0.5) * result["score"], 3)

# Lightweight burnout-language keyword set (supplements RoBERTa)
_STRESS_KEYWORDS = {
    "deadline", "stuck", "broken", "blocked", "overtime", "exhausted",
    "behind", "overloaded", "cant", "urgent", "escalation", "terrible",
    "nightmare", "swamped", "drowning", "overwhelmed", "burned", "hate",
}

def _stress_score(text: str) -> float:
    """0–1: fraction of stress-keyword hits, capped at 1."""
    words = set(text.lower().split())
    return min(1.0, len(words & _STRESS_KEYWORDS) / 3.0)

# ---------------------------------------------------------------------------
# Loaders
# ---------------------------------------------------------------------------
def load_employees():
    with open(DATA_DIR / "employees.csv") as f:
        return {r["employee_id"]: r for r in csv.DictReader(f)}

def load_slack():
    rows = []
    with open(DATA_DIR / "slack_messages.csv") as f:
        for r in csv.DictReader(f):
            r["timestamp"]       = datetime.strptime(r["timestamp"], "%Y-%m-%d %H:%M:%S")
            r["message_length"]  = int(r["message_length"])
            r["sentiment_score"] = float(r["sentiment_score"])
            r["is_dm"]           = r["is_dm"] == "true"
            rows.append(r)
    return rows

def load_jira():
    rows = []
    with open(DATA_DIR / "jira_events.csv") as f:
        for r in csv.DictReader(f):
            r["timestamp"]    = datetime.strptime(r["timestamp"], "%Y-%m-%d %H:%M:%S")
            r["story_points"] = int(r["story_points"])
            rows.append(r)
    return rows

def load_calendar():
    rows = []
    with open(DATA_DIR / "calendar_events.csv") as f:
        for r in csv.DictReader(f):
            r["date"]           = datetime.strptime(r["date"], "%Y-%m-%d").date()
            r["start_min"]      = (int(r["start_time"].split(":")[0]) * 60
                                   + int(r["start_time"].split(":")[1]))
            r["end_min"]        = (int(r["end_time"].split(":")[0]) * 60
                                   + int(r["end_time"].split(":")[1]))
            r["attendee_count"] = int(r["attendee_count"])
            rows.append(r)
    return rows

def load_git():
    path = DATA_DIR / "git_commits.csv"
    if not path.exists():
        return []
    rows = []
    with open(path) as f:
        for r in csv.DictReader(f):
            r["timestamp"]     = datetime.strptime(r["timestamp"], "%Y-%m-%d %H:%M:%S")
            r["lines_added"]   = int(r["lines_added"])
            r["lines_deleted"] = int(r["lines_deleted"])
            rows.append(r)
    return rows

def load_zoom():
    path = DATA_DIR / "zoom_meetings.csv"
    if not path.exists():
        return []
    rows = []
    with open(path) as f:
        for r in csv.DictReader(f):
            r["date"]              = datetime.strptime(r["date"], "%Y-%m-%d").date()
            r["duration_minutes"]  = int(r["duration_minutes"])
            r["speaking_seconds"]  = int(r["speaking_seconds"])
            r["participant_count"] = int(r["participant_count"])
            rows.append(r)
    return rows

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------
def _is_after_hours(dt: datetime) -> bool:
    return dt.hour >= AFTER_HOURS_START or dt.hour < AFTER_HOURS_END

def _is_weekend(d) -> bool:
    if isinstance(d, datetime):
        d = d.date()
    return d.weekday() >= 5

# ---------------------------------------------------------------------------
# Feature computation — Slack
# ---------------------------------------------------------------------------
def compute_slack_features(slack_rows):
    """
    Per (employee, date) slack features.
    New vs V3.2: slack_sentiment_volatility, slack_avg_stress_score.
    Sentiment is now RoBERTa-scored (or fallback) per message.
    """
    acc = defaultdict(lambda: {
        "total": 0, "after_hours": 0, "weekend": 0,
        "work_ch": 0, "social_ch": 0, "dms": 0,
        "channels": set(), "dm_partners": set(),
        "sentiments": [], "stresses": [],
    })

    for r in slack_rows:
        key = (r["employee_id"], r["timestamp"].date())
        a   = acc[key]
        a["total"] += 1

        if _is_after_hours(r["timestamp"]):
            a["after_hours"] += 1
        if _is_weekend(r["timestamp"]):
            a["weekend"] += 1

        if r["is_dm"]:
            a["dms"] += 1
            if r["recipient_id"]:
                a["dm_partners"].add(r["recipient_id"])
        else:
            a["channels"].add(r["channel"])
            if r["channel"] in SOCIAL_CHANNELS:
                a["social_ch"] += 1
            else:
                a["work_ch"] += 1

        text      = r.get("message_text", "")
        fallback  = r["sentiment_score"]
        sent      = _score_text(text, fallback)
        stress    = _stress_score(text)
        a["sentiments"].append(sent)
        a["stresses"].append(stress)

    out = {}
    for key, a in acc.items():
        n          = len(a["sentiments"])
        avg_sent   = round(sum(a["sentiments"]) / n, 3) if n else 0.0
        avg_stress = round(sum(a["stresses"]) / n, 3)   if n else 0.0
        volatility = round(_stats.stdev(a["sentiments"]), 3) if n >= 2 else 0.0

        out[key] = {
            "slack_msgs_total":              a["total"],
            "slack_msgs_after_hours":        a["after_hours"],
            "slack_msgs_weekend":            a["weekend"],
            "slack_msgs_work_channels":      a["work_ch"],
            "slack_msgs_social_channels":    a["social_ch"],
            "slack_dms_sent":                a["dms"],
            "slack_distinct_channels":       len(a["channels"]),
            "slack_distinct_dm_partners":    len(a["dm_partners"]),
            "slack_avg_sentiment":           avg_sent,
            "slack_sentiment_volatility":    volatility,
            "slack_avg_stress_score":        avg_stress,
        }
    return out

# ---------------------------------------------------------------------------
# Feature computation — Jira (unchanged)
# ---------------------------------------------------------------------------
def compute_jira_features(jira_rows):
    acc = defaultdict(lambda: {
        "events": 0, "tickets": set(), "closed": 0, "sp_closed": 0, "comments": 0,
    })
    for r in jira_rows:
        key = (r["employee_id"], r["timestamp"].date())
        a   = acc[key]
        a["events"] += 1
        a["tickets"].add(r["ticket_id"])
        if r["event_type"] == "comment":
            a["comments"] += 1
        if r["to_status"] == "Done":
            a["closed"] += 1
            a["sp_closed"] += r["story_points"]

    return {
        key: {
            "jira_events":              a["events"],
            "jira_distinct_tickets":    len(a["tickets"]),
            "jira_tickets_closed":      a["closed"],
            "jira_story_points_closed": a["sp_closed"],
            "jira_comments":            a["comments"],
        }
        for key, a in acc.items()
    }

# ---------------------------------------------------------------------------
# Feature computation — Calendar (unchanged)
# ---------------------------------------------------------------------------
def compute_calendar_features(cal_rows):
    by_key = defaultdict(list)
    for r in cal_rows:
        by_key[(r["employee_id"], r["date"])].append(r)

    WORK_START, WORK_END = 9 * 60, 18 * 60
    out = {}
    for key, meetings in by_key.items():
        meetings.sort(key=lambda m: m["start_min"])
        total_min = sum(m["end_min"] - m["start_min"] for m in meetings)
        attendees = sum(m["attendee_count"]             for m in meetings)

        b2b = sum(
            1 for i in range(1, len(meetings))
            if 0 <= meetings[i]["start_min"] - meetings[i-1]["end_min"] < 15
        )

        intervals = sorted(
            (max(m["start_min"], WORK_START), min(m["end_min"], WORK_END))
            for m in meetings
            if max(m["start_min"], WORK_START) < min(m["end_min"], WORK_END)
        )
        cursor, largest_gap = WORK_START, 0
        for s, e in intervals:
            if s > cursor:
                largest_gap = max(largest_gap, s - cursor)
            cursor = max(cursor, e)
        if cursor < WORK_END:
            largest_gap = max(largest_gap, WORK_END - cursor)
        if not meetings:
            largest_gap = WORK_END - WORK_START

        out[key] = {
            "meetings_count":            len(meetings),
            "meetings_total_minutes":    total_min,
            "meetings_attendees_total":  attendees,
            "meetings_back_to_back":     b2b,
            "largest_focus_gap_minutes": largest_gap,
        }
    return out

